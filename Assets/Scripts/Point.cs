//////////////////////////////////////////////////////////////////////

public class Point
{
    //////////////////////////////////////////////////////////////////////

    public int x, y;

    //////////////////////////////////////////////////////////////////////

    public Point()
    {

    }

    //////////////////////////////////////////////////////////////////////

    public Point(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    //////////////////////////////////////////////////////////////////////

    public static Point operator +(Point a, Point b)
    {
        return new Point(a.x + b.x, a.y + b.y);
    }
}
